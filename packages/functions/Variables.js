
//There is no need to declare a variable with a type you can just simply use var

var greeting; //Declaration
console.log(greeting)  //output: undefined
greeting = "Hello!"; //Declaration
console.log(greeting); //Hello!


var name = "John";   //string
var age = 20;        //number
var isMarried = false; //boolean
var salary = null;   //null
var kids;            //undefined

console.log(name);   //John
console.log(age);    //20
console.log(isMarried); //false
console.log(salary);   //null
console.log(kids);   //undefined
